/*
Devuelve el valor de la matriz para una posición dada en pixels
*/
Balls.prototype.getBall = function(x,y){
    let col = Math.floor(x / TILE_SIZE);
    let row = Math.floor(y / TILE_SIZE);
    let i = row * NCOLS + col;
    return this.matrix[i];
}


/*
Asigna un valor a la bolita en la posición dada en pixels y devuelve el valor que hubiera antes
*/
Balls.prototype.setBall = function(x,y, value){
    let col = Math.floor(x / TILE_SIZE) +1;
    let row = Math.floor(y / TILE_SIZE) +1;
    if(this.last_ball_accessed != undefined && this.last_ball_accessed[0] == row && this.last_ball_accessed[1] == col){
        return -1;
    }

    this.last_ball_accessed = [row,col];
    let i = row * NCOLS + col;
    let aux = this.matrix[i];
    this.matrix[i] = value;
    return aux;
}